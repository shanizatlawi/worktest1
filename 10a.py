income = float(input("Enter income: "))
tax = 0.0

if income <= 6000:
    tax = 0
elif income <= 12000:
    tax = 0.1 * (income - 6000)
elif income <= 18000:
    tax = 0.1 * (12000 - 6000) + 0.2 * (income - 12000)
elif income <= 25000:
    tax = 0.1 * (12000 - 6000) + 0.2 * (18000 - 12000) + 0.3 * (income - 18000)
elif income <= 35000:
    tax = 0.1 * (12000 - 6000) + 0.2 * (18000 - 12000) + 0.3 * (25000 - 18000) + 0.4 * (income - 25000)
elif income <= 50000:
    tax = 0.1 * (12000 - 6000) + 0.2 * (18000 - 12000) + 0.3 * (25000 - 18000) + 0.4 * (35000 - 25000) + 0.45 * (income - 35000)
else:
    tax = 0.1 * (12000 - 6000) + 0.2 * (18000 - 12000) + 0.3 * (25000 - 18000) + 0.4 * (35000 - 25000) + 0.45 * (50000 - 35000) + 0.51 * (income - 50000)

print(f"Tax due: {tax:.2f}")


